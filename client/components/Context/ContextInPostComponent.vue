<script setup lang="ts">
import router from "../../router";

const props = defineProps(["context"]);
const emit = defineEmits(["refreshContexts"]);

function openUser() {
  if (props.context.author !== "Autogenerated") {
    void router.push({ path: "/user", query: { username: props.context.author } });
  }
}
</script>

<template>
  <p @click="openUser" class="author">{{ props.context.author }}</p>
  <p>{{ props.context.content }}</p>
</template>

<style scoped>
p {
  margin: 0em;
}

.author {
  font-weight: bold;
  font-size: 1.2em;
}
</style>
